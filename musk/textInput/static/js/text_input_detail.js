// For Heading
function HeadingIncrement() {
    var i = document.getElementById('HeadingTextbox').value
    document.getElementById('HeadingTextbox').value = ++i;
}
function HeadingDecrement() {
    var i = document.getElementById('HeadingTextbox').value
    document.getElementById('HeadingTextbox').value = --i;
}
const HeadingCheckbox = document.getElementById('HeadingCheckbox');
const HeadingBox = document.getElementById('HeadingDiv');
HeadingCheckbox.addEventListener('click', function handleClick() {
if (HeadingCheckbox.checked) {
    HeadingBox.style.visibility = 'visible';
} else {
    HeadingBox.style.visibility = 'hidden';
}
});


